package cahp12;

public class JavaDoc {
    /*
    * 클래스 선언부  : 클래스가 어떻게 정의되어 있나
    *
    * 클래스가 추상클래스인지 final 클래스인지
    * 부모 클래스, 인터페이스
    *
    *
    *  java.base
    *  java의 모든 모듈이 의존하는 기본 모듈
    *
    *  java Object 클래스
    *  자바 클래스의 최상위 클래스
    *  클래스를 선언할 때 extends 키워드로 다른 클래스를 상속하지 않으면 암시적으로 Object 클래스를
    *  상속 받는다.
    *
    *  toString : 객체의 문자열 반환
    *  equals : 두 객체가 같은지 비교
    *  hashCode : 객체의 해시코드를 반환
    *
    *
    *  System 클래스
    *  java.lang 운영체제의 일부기능을 이용할 수 있다.
    *  out 콘솔에 문자 출력
    *  in  키보드 입력
    *  err 콘솔에 에러 내용 출력
    *
    *
    *  문자열 클래스
    *  String         : 문자열 저장, 조작할 때 사용
    *  StringBuilder
    *  StringTokenizer
    *
    *  Wrapper 클래스
    *  자바의 기본 데이터 타입을 객체로 다룰 수 있게 해주는 Wrapper 클래스
    *  byte short int long float double char boolean
    *  Byte Short     Long Float Double      Boolean
    *            Integer                Character
    *
    *  기본 타입의 값을 래퍼클래스로 만드는 과정을 Boxing
    *  래퍼클래스에서 기본 타입의 값을 가져오는 과정을 Unboxing
    *
    *  Math 클래스
    *  수학 계산에 사용할 수 있는 메소드를 제공
    *  전부 static Math 클래스로 바로 사용 가능함
    *
    *  날짜와 시간 클래스
    *  Date      : 날짜 정보
    *  Calendar  : 다양한 시간대별로 날짜와 시간
    *  LocalDateTime : 날짜와 시간 조작
    *
    * */

}
